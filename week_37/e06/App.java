public class App {
    public static void main(String[] args) {
        boolean isJavaFun = true;
        boolean isFishTasty = false;
        System.out.println("isJavaFun && isFishTasty: " + (isJavaFun && isFishTasty)); // AND
        System.out.println("isJavaFun || isFishTasty: " + (isJavaFun || isFishTasty)); // OR
        System.out.println("!isJavaFun: " + (!isJavaFun)); // NOT
        System.out.println("!isFishTasty: " + (!isFishTasty)); // NOT
    }
}
